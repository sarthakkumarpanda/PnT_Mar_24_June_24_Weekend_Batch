package com.java.Day3_23rd_Mar_2024_Basics_of_Java;

public class Topic6_Application_of_DataTypes {

	public static void main(String[] args) {
	
		//addition
		//subtraction
		//multiplication
		//division
		//to the power
		//remainder
		
		//Integers
		int number1 = 100;
		int number2 = 200;
		System.out.println(number1 + number2);
		System.out.println(number1 - number2);
		System.out.println(number1 * number2);
		System.out.println(number1 / number2);
		System.out.println(number1 % number2);
		
		System.out.println("***************************************************************************");
		
		//Double
		double d1 = 10.10;
		double d2 = 1.1;
		System.out.println(d1 + d2);
		System.out.println(d1 - d2);
		System.out.println(d1 * d2);
		System.out.println(d1 / d2);
		System.out.println(d1 % d2);
		

	}

}
